#!/usr/bin/env node

import { spawn } from "node:child_process";
import { existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import os from "node:os";
import readline from "node:readline";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const defaultGitRepo = "https://github.com/gusibi/molibot";
const defaultGitRef = "master";
const defaultDataDir = join(os.homedir(), ".molibot");
const defaultDeployDir = join(os.homedir(), "molibot");
const deployConfigPath = resolvePath(process.env.MOLIBOT_DEPLOY_CONFIG || join(resolveDataDir(), "deploy.env"));

function expandHomePath(input) {
  if (!input.startsWith("~")) return input;
  if (input === "~") return os.homedir();
  if (input.startsWith("~/")) return join(os.homedir(), input.slice(2));
  return input;
}

function resolvePath(input) {
  return resolve(expandHomePath(String(input || "").trim()));
}

function resolveDataDir() {
  return resolvePath(process.env.DATA_DIR || defaultDataDir);
}

function parseEnvValue(raw) {
  const value = raw.trim();
  if ((value.startsWith('"') && value.endsWith('"')) || (value.startsWith("'") && value.endsWith("'"))) {
    return value.slice(1, -1);
  }
  return value;
}

function loadDeployConfig() {
  const config = {};
  if (!existsSync(deployConfigPath)) return config;
  const lines = readFileSync(deployConfigPath, "utf8").split(/\r?\n/);
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq <= 0) continue;
    config[trimmed.slice(0, eq)] = parseEnvValue(trimmed.slice(eq + 1));
  }
  return config;
}

function shellQuote(value) {
  return `'${String(value).replace(/'/g, "'\\''")}'`;
}

function saveDeployConfig(config) {
  mkdirSync(dirname(deployConfigPath), { recursive: true });
  const orderedKeys = [
    "MOLIBOT_GIT_REPO",
    "MOLIBOT_GIT_REF",
    "MOLIBOT_DEPLOY_DIR",
    "MOLIBOT_CURRENT_LINK",
    "DATA_DIR",
    "PORT",
    "MOLIBOT_LOG_FILE",
    "MOLIBOT_PID_FILE"
  ];
  const lines = [
    "# Molibot deployment config generated by molibot manage",
    ...orderedKeys.map((key) => `${key}=${shellQuote(config[key] || "")}`)
  ];
  writeFileSync(deployConfigPath, `${lines.join("\n")}\n`, "utf8");
}

function withDefaults(config) {
  const deployDir = resolvePath(config.MOLIBOT_DEPLOY_DIR || defaultDeployDir);
  return {
    MOLIBOT_GIT_REPO: config.MOLIBOT_GIT_REPO || defaultGitRepo,
    MOLIBOT_GIT_REF: config.MOLIBOT_GIT_REF || defaultGitRef,
    MOLIBOT_DEPLOY_DIR: deployDir,
    MOLIBOT_CURRENT_LINK: resolvePath(config.MOLIBOT_CURRENT_LINK || join(deployDir, "current")),
    DATA_DIR: resolvePath(config.DATA_DIR || defaultDataDir),
    PORT: config.PORT || "3000",
    MOLIBOT_LOG_FILE: resolvePath(config.MOLIBOT_LOG_FILE || join(os.homedir(), "logs", "molibot.log")),
    MOLIBOT_PID_FILE: resolvePath(config.MOLIBOT_PID_FILE || join(defaultDataDir, "molibot.pid"))
  };
}

function buildEnv(config) {
  return {
    ...process.env,
    ...config,
    MOLIBOT_APP_DIR: config.MOLIBOT_CURRENT_LINK
  };
}

function run(command, args, env, allowFailure = false) {
  return new Promise((resolveRun) => {
    const child = spawn(command, args, {
      cwd: rootDir,
      stdio: "inherit",
      env
    });
    child.on("exit", (code, signal) => {
      if (signal) {
        console.error(`Command interrupted by ${signal}`);
        resolveRun(false);
        return;
      }
      if (code === 0 || allowFailure) {
        resolveRun(code === 0);
        return;
      }
      console.error(`Command failed with exit code ${code}`);
      resolveRun(false);
    });
  });
}

function createPrompt() {
  return readline.createInterface({
    input: process.stdin,
    output: process.stdout
  });
}

function ask(rl, question, fallback = "") {
  const suffix = fallback ? ` [${fallback}]` : "";
  return new Promise((resolveAsk) => {
    rl.question(`${question}${suffix}: `, (answer) => {
      const trimmed = answer.trim();
      resolveAsk(trimmed || fallback);
    });
  });
}

async function pause(rl) {
  await ask(rl, "Press Enter to continue");
}

function printHelp() {
  console.log([
    "Molibot manager",
    "",
    "Usage:",
    "  molibot manage",
    "",
    "Provides a lightweight interactive menu for configure/install/update/start/stop/restart/status/logs/uninstall."
  ].join("\n"));
}

function printMenu(config) {
  console.log("");
  console.log("Molibot Manager");
  console.log(`Config: ${deployConfigPath}`);
  console.log(`Deploy dir: ${config.MOLIBOT_DEPLOY_DIR}`);
  console.log(`Current: ${config.MOLIBOT_CURRENT_LINK}`);
  console.log("");
  console.log("1. Configure deployment");
  console.log("2. Install or update from GitHub");
  console.log("3. Start service");
  console.log("4. Stop service");
  console.log("5. Restart service");
  console.log("6. Show status");
  console.log("7. Show logs");
  console.log("8. Uninstall runtime files");
  console.log("9. Exit");
  console.log("");
}

async function configure(rl, config) {
  const next = { ...config };
  next.MOLIBOT_GIT_REPO = await ask(rl, "GitHub repository URL", next.MOLIBOT_GIT_REPO);
  next.MOLIBOT_GIT_REF = await ask(rl, "Git ref/branch", next.MOLIBOT_GIT_REF);
  next.MOLIBOT_DEPLOY_DIR = resolvePath(await ask(rl, "Deploy directory", next.MOLIBOT_DEPLOY_DIR));
  next.MOLIBOT_CURRENT_LINK = resolvePath(
    await ask(rl, "Current release symlink", next.MOLIBOT_CURRENT_LINK || join(next.MOLIBOT_DEPLOY_DIR, "current"))
  );
  next.DATA_DIR = resolvePath(await ask(rl, "Data directory", next.DATA_DIR));
  next.PORT = await ask(rl, "Port", next.PORT);
  next.MOLIBOT_LOG_FILE = resolvePath(await ask(rl, "Log file", next.MOLIBOT_LOG_FILE));
  next.MOLIBOT_PID_FILE = resolvePath(await ask(rl, "PID file", next.MOLIBOT_PID_FILE));
  saveDeployConfig(next);
  console.log(`Saved: ${deployConfigPath}`);
  return next;
}

async function installOrUpdate(rl, config) {
  await run(join(rootDir, "bin", "molibot-update.sh"), [], buildEnv(config));
  return config;
}

async function service(command, config) {
  const releaseService = join(config.MOLIBOT_CURRENT_LINK, "bin", "molibot-service.sh");
  const serviceScript = existsSync(releaseService)
    ? releaseService
    : join(rootDir, "bin", "molibot-service.sh");
  await run(serviceScript, [command], buildEnv(config), command === "status");
}

async function showLogs(config) {
  const logFile = config.MOLIBOT_LOG_FILE;
  if (!existsSync(logFile)) {
    console.log(`Log file not found: ${logFile}`);
    return;
  }
  await run("tail", ["-n", "120", logFile], process.env, true);
}

function canRemoveDeployDir(config) {
  const deployDir = resolvePath(config.MOLIBOT_DEPLOY_DIR);
  const dataDir = resolvePath(config.DATA_DIR);
  const marker = join(deployDir, ".molibot-deploy");
  return deployDir !== "/" &&
    deployDir !== os.homedir() &&
    deployDir !== dataDir &&
    deployDir.startsWith(os.homedir()) &&
    existsSync(marker);
}

async function uninstallRuntime(rl, config) {
  console.log("This removes only runtime deployment files.");
  console.log(`Will remove: ${config.MOLIBOT_DEPLOY_DIR}`);
  console.log(`Will keep data directory: ${config.DATA_DIR}`);
  if (!canRemoveDeployDir(config)) {
    console.log("Refusing to remove deploy directory because it is too broad, overlaps DATA_DIR, or lacks .molibot-deploy marker.");
    return;
  }
  const confirmation = await ask(rl, 'Type "remove runtime" to confirm');
  if (confirmation !== "remove runtime") {
    console.log("Uninstall cancelled.");
    return;
  }
  await service("stop", config);
  rmSync(config.MOLIBOT_DEPLOY_DIR, { recursive: true, force: true });
  console.log("Runtime files removed. DATA_DIR was kept.");
}

async function main() {
  if (process.argv.includes("--help") || process.argv.includes("-h")) {
    printHelp();
    return;
  }

  let config = withDefaults(loadDeployConfig());
  const rl = createPrompt();
  try {
    while (true) {
      printMenu(config);
      const choice = await ask(rl, "Choose");
      if (choice === "1") config = await configure(rl, config);
      else if (choice === "2") config = await installOrUpdate(rl, config);
      else if (choice === "3") await service("start", config);
      else if (choice === "4") await service("stop", config);
      else if (choice === "5") await service("restart", config);
      else if (choice === "6") await service("status", config);
      else if (choice === "7") await showLogs(config);
      else if (choice === "8") await uninstallRuntime(rl, config);
      else if (choice === "9" || choice.toLowerCase() === "q") break;
      else console.log("Unknown choice.");
      if (choice !== "9" && choice.toLowerCase() !== "q") await pause(rl);
    }
  } finally {
    rl.close();
  }
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
